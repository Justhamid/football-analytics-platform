def normaliser_nom(nom: str) -> str:
    """
    Normalise un nom d'équipe vers sa forme canonique.
    Si le nom n'est pas dans le dictionnaire, on le retourne tel quel
    en loggant un avertissement.
    """
    if not isinstance(nom, str):
        return nom

    nom_strip = nom.strip()

    if nom_strip in TEAM_MAPPING:
        return TEAM_MAPPING[nom_strip]

    # Tentative de correspondance insensible à la casse
    for cle, valeur in TEAM_MAPPING.items():
        if cle.lower() == nom_strip.lower():
            return valeur

    # Nom non trouvé — on le retourne tel quel
    return nom_strip